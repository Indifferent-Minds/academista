let ordenDescendente = true;

const container = document.getElementById("librosContainer");
const ordenarBtn = document.getElementById("ordenarBtn");
const textoOrdenar = document.getElementById("buscarlibro-texto-ordenar");

function renderLibros(libros) {
  container.innerHTML = "";
  libros.forEach(libro => {
    const div = document.createElement("div");
    div.className = "libro";

    div.innerHTML = `
      <img src="${libro.thumbnail}" alt="${libro.title}">
      <div class="libro-info">
        <h3>${libro.title}</h3>
        <p><strong>Autor:</strong> ${libro.autor}</p>
        <p><strong>Fecha:</strong> ${libro.fecha}</p>
        <a href="${libro.pdf}" target="_blank">Ver PDF</a>
      </div>
    `;

    container.appendChild(div);
  });
}

function ordenarPorFecha() {
  ordenDescendente = !ordenDescendente;
  const librosOrdenados = [...librosData].sort((a, b) => {
    return ordenDescendente
      ? new Date(b.fecha) - new Date(a.fecha)
      : new Date(a.fecha) - new Date(b.fecha);
  });

  textoOrdenar.textContent = ordenDescendente ? "Más recientes" : "Más antiguos";
  renderLibros(librosOrdenados);
}

ordenarBtn.addEventListener("click", ordenarPorFecha);

// Inicial
ordenarPorFecha();
